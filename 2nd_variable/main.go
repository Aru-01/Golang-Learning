package main

import "fmt"

func main() {
	var a int = 10                //long version (int)
	var s string = "Hello World!" //long version (str)
	var b float64 = 64.52         //long version (float)
	// fmt.Println(a)
	fmt.Println(a, s, b)

	c := false // shorter version (boolean)
	fmt.Println(c)

}
